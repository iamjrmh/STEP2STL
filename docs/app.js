/**
 * STEP2STL - app.js
 * Converts .step/.stp files to binary STL in the browser.
 *
 * Library: occt-import-js v0.0.23
 *   JS:   https://cdn.jsdelivr.net/npm/occt-import-js@0.0.23/dist/occt-import-js.js
 *   WASM: https://cdn.jsdelivr.net/npm/occt-import-js@0.0.23/dist/occt-import-js.wasm
 *
 * occt-import-js reads STEP files and returns a triangle mesh as JSON.
 * We then hand-assemble a binary STL from those triangles.
 *
 * Folder drag-and-drop uses webkitGetAsEntry() to traverse recursively.
 */

'use strict';

// ── occt-import-js loader ────────────────────────────────────────────────────
const OCCT_JS   = 'https://cdn.jsdelivr.net/npm/occt-import-js@0.0.23/dist/occt-import-js.js';
const OCCT_WASM = 'https://cdn.jsdelivr.net/npm/occt-import-js@0.0.23/dist/occt-import-js.wasm';

let ocPromise = null;

function loadOpenCascade() {
  if (ocPromise) return ocPromise;
  ocPromise = (async () => {
    log('dim', '  Loading occt-import-js…');
    // Load the UMD bundle via a classic script tag so the global
    // `occtimportjs` function is available on window.
    await new Promise((resolve, reject) => {
      if (window.occtimportjs) { resolve(); return; }
      const s = document.createElement('script');
      s.src = OCCT_JS;
      s.onload = resolve;
      s.onerror = () => reject(new Error('Could not load occt-import-js - check your internet connection'));
      document.head.appendChild(s);
    });
    log('dim', '  Initialising WASM kernel…');
    try {
      const occt = await window.occtimportjs({
        locateFile: (f) => f.endsWith('.wasm') ? OCCT_WASM : f,
      });
      log('success', '✓ OpenCASCADE kernel ready');
      return occt;
    } catch (err) {
      throw new Error('WASM init failed: ' + err.message);
    }
  })();
  ocPromise.catch(() => { ocPromise = null; });
  return ocPromise;
}

// ── STEP → binary STL ───────────────────────────────────────────────────────
// occt-import-js gives us back a JSON mesh (positions + indices).
// We convert that into the 80-byte-header + triangle-count + triangles
// binary STL format.
function convertStepToStl(occt, arrayBuffer, filename) {
  const fileBuffer = new Uint8Array(arrayBuffer);
  const result = occt.ReadStepFile(fileBuffer, null);

  if (!result.success) {
    throw new Error('STEP reader returned error - file may be malformed');
  }

  // Collect all triangles across all meshes.
  // result.meshes[i].attributes.position.array  → flat Float32 vertex array
  // result.meshes[i].index.array                → flat index array (triplets)
  let totalTriangles = 0;
  for (const mesh of result.meshes) {
    if (mesh.index && mesh.index.array) {
      totalTriangles += mesh.index.array.length / 3;
    } else if (mesh.attributes && mesh.attributes.position) {
      totalTriangles += mesh.attributes.position.array.length / 9;
    }
  }

  if (totalTriangles === 0) {
    throw new Error('STEP file parsed but produced no geometry');
  }

  // Binary STL layout:
  //   80 bytes header
  //   4 bytes uint32 triangle count
  //   50 bytes × N triangles  (12 normal + 3×12 vertices + 2 attribute)
  const bufSize = 84 + totalTriangles * 50;
  const buf = new ArrayBuffer(bufSize);
  const view = new DataView(buf);

  // Header (80 bytes) - just ASCII zeros, not read by viewers
  const headerText = 'Binary STL generated by STEP2STL';
  for (let i = 0; i < Math.min(headerText.length, 80); i++) {
    view.setUint8(i, headerText.charCodeAt(i));
  }

  view.setUint32(80, totalTriangles, true);

  let offset = 84;

  for (const mesh of result.meshes) {
    const pos = mesh.attributes && mesh.attributes.position
      ? mesh.attributes.position.array
      : null;
    if (!pos) continue;

    const idx = mesh.index ? mesh.index.array : null;
    const triCount = idx ? idx.length / 3 : pos.length / 9;

    for (let t = 0; t < triCount; t++) {
      let ax, ay, az, bx, by, bz, cx, cy, cz;

      if (idx) {
        const ia = idx[t * 3] * 3;
        const ib = idx[t * 3 + 1] * 3;
        const ic = idx[t * 3 + 2] * 3;
        ax = pos[ia]; ay = pos[ia + 1]; az = pos[ia + 2];
        bx = pos[ib]; by = pos[ib + 1]; bz = pos[ib + 2];
        cx = pos[ic]; cy = pos[ic + 1]; cz = pos[ic + 2];
      } else {
        const base = t * 9;
        ax = pos[base];     ay = pos[base + 1]; az = pos[base + 2];
        bx = pos[base + 3]; by = pos[base + 4]; bz = pos[base + 5];
        cx = pos[base + 6]; cy = pos[base + 7]; cz = pos[base + 8];
      }

      // Compute face normal via cross product of (B−A) × (C−A)
      const ux = bx - ax, uy = by - ay, uz = bz - az;
      const vx = cx - ax, vy = cy - ay, vz = cz - az;
      let nx = uy * vz - uz * vy;
      let ny = uz * vx - ux * vz;
      let nz = ux * vy - uy * vx;
      const len = Math.sqrt(nx * nx + ny * ny + nz * nz) || 1;
      nx /= len; ny /= len; nz /= len;

      // Normal (12 bytes)
      view.setFloat32(offset,      nx, true); offset += 4;
      view.setFloat32(offset,      ny, true); offset += 4;
      view.setFloat32(offset,      nz, true); offset += 4;
      // Vertex A
      view.setFloat32(offset, ax, true); offset += 4;
      view.setFloat32(offset, ay, true); offset += 4;
      view.setFloat32(offset, az, true); offset += 4;
      // Vertex B
      view.setFloat32(offset, bx, true); offset += 4;
      view.setFloat32(offset, by, true); offset += 4;
      view.setFloat32(offset, bz, true); offset += 4;
      // Vertex C
      view.setFloat32(offset, cx, true); offset += 4;
      view.setFloat32(offset, cy, true); offset += 4;
      view.setFloat32(offset, cz, true); offset += 4;
      // Attribute byte count (2 bytes, always 0)
      view.setUint16(offset, 0, true); offset += 2;
    }
  }

  return new Uint8Array(buf);
}

// ── Folder drag-and-drop ─────────────────────────────────────────────────────
function readEntry(entry) {
  return new Promise((resolve) => {
    if (entry.isFile) {
      entry.file(f => resolve([f]), () => resolve([]));
    } else if (entry.isDirectory) {
      const reader = entry.createReader();
      const all = [];
      function readAll() {
        reader.readEntries(async (entries) => {
          if (!entries.length) {
            const nested = await Promise.all(all.map(readEntry));
            resolve(nested.flat());
          } else {
            all.push(...entries);
            readAll();
          }
        }, () => resolve([]));
      }
      readAll();
    } else {
      resolve([]);
    }
  });
}

async function getFilesFromDataTransfer(dt) {
  const files = [];
  // Modern API - supports folders
  if (dt.items && dt.items.length && dt.items[0].webkitGetAsEntry) {
    const promises = [];
    for (const item of dt.items) {
      const entry = item.webkitGetAsEntry();
      if (entry) promises.push(readEntry(entry));
    }
    const results = await Promise.all(promises);
    files.push(...results.flat());
  } else {
    // Fallback - plain file list (no folders)
    files.push(...Array.from(dt.files));
  }
  return files;
}

// ── ZIP builder (dependency-free) ───────────────────────────────────────────
(function buildZipModule() {
  const CRC = (() => {
    const t = new Uint32Array(256);
    for (let i = 0; i < 256; i++) {
      let c = i;
      for (let j = 0; j < 8; j++) c = (c & 1) ? (0xedb88320 ^ (c >>> 1)) : (c >>> 1);
      t[i] = c;
    }
    return t;
  })();

  function crc32(data) {
    let c = 0xffffffff;
    for (let i = 0; i < data.length; i++) c = CRC[(c ^ data[i]) & 0xff] ^ (c >>> 8);
    return (c ^ 0xffffffff) >>> 0;
  }

  function u16(n) { const b = new Uint8Array(2); new DataView(b.buffer).setUint16(0, n, true); return b; }
  function u32(n) { const b = new Uint8Array(4); new DataView(b.buffer).setUint32(0, n, true); return b; }
  function cat(...a) {
    const t = a.reduce((s, x) => s + x.length, 0), o = new Uint8Array(t); let off = 0;
    for (const x of a) { o.set(x, off); off += x.length; }
    return o;
  }

  window.buildZip = function(stlFiles, origFiles) {
    const enc = new TextEncoder();
    const entries = [
      ...stlFiles.map(f  => ({ path: f.name,              data: f.data })),
      ...origFiles.map(f => ({ path: 'originals/' + f.name, data: f.data })),
    ];

    const locals = [], centrals = [];
    let off = 0;

    for (const e of entries) {
      const p = enc.encode(e.path), crc = crc32(e.data), sz = e.data.length;
      const lh = cat(
        new Uint8Array([0x50,0x4b,0x03,0x04]),
        u16(20), u16(0), u16(0), u16(0), u16(0),
        u32(crc), u32(sz), u32(sz),
        u16(p.length), u16(0), p
      );
      const cd = cat(
        new Uint8Array([0x50,0x4b,0x01,0x02]),
        u16(20), u16(20), u16(0), u16(0), u16(0), u16(0),
        u32(crc), u32(sz), u32(sz),
        u16(p.length), u16(0), u16(0), u16(0), u16(0), u32(0), u32(off), p
      );
      locals.push(cat(lh, e.data));
      centrals.push(cd);
      off += lh.length + sz;
    }

    const cdData = cat(...centrals);
    const eocd = cat(
      new Uint8Array([0x50,0x4b,0x05,0x06]),
      u16(0), u16(0), u16(entries.length), u16(entries.length),
      u32(cdData.length), u32(off), u16(0)
    );
    return cat(...locals, cdData, eocd);
  };
})();

// ── Download helper ──────────────────────────────────────────────────────────
function downloadBlob(data, name, mime) {
  const url = URL.createObjectURL(new Blob([data], { type: mime }));
  const a = Object.assign(document.createElement('a'), { href: url, download: name });
  document.body.appendChild(a);
  a.click();
  setTimeout(() => { URL.revokeObjectURL(url); a.remove(); }, 1500);
}

// ── UI wiring ────────────────────────────────────────────────────────────────
const $ = id => document.getElementById(id);

const dropzone    = $('dropzone');
const fileInput   = $('fileInput');
const browseBtn   = $('browseBtn');
const queueEl     = $('queue');
const queueCount  = $('queueCount');
const fileListEl  = $('fileList');
const convertBtn  = $('convertBtn');
const clearBtn    = $('clearBtn');
const logPanel    = $('logPanel');
const logBody     = $('logBody');
const logClearBtn = $('logClearBtn');
const scanLine    = $('scanLine');

let fileQueue = [];

function log(type, msg) {
  logPanel.hidden = false;
  const el = document.createElement('span');
  el.className = 'log-line ' + type;
  el.textContent = msg;
  logBody.appendChild(el);
  logBody.scrollTop = logBody.scrollHeight;
}

function fmt(bytes) {
  if (bytes < 1024)    return bytes + ' B';
  if (bytes < 1048576) return (bytes/1024).toFixed(1) + ' KB';
  return (bytes/1048576).toFixed(1) + ' MB';
}

function renderQueue() {
  fileListEl.innerHTML = '';
  if (!fileQueue.length) {
    queueEl.hidden = true;
    dropzone.hidden = false;
    return;
  }
  dropzone.hidden = true;
  queueEl.hidden = false;
  queueCount.textContent = fileQueue.length + ' file' + (fileQueue.length !== 1 ? 's' : '');
  fileQueue.forEach((f, i) => {
    const li = document.createElement('li');
    li.id = 'fi-' + i;
    li.innerHTML = `
      <span class="file-name" title="${f.name}">${f.name}</span>
      <span class="file-size">${fmt(f.size)}</span>
      <span class="file-status pending" id="fs-${i}">pending</span>
      <button class="file-remove" data-i="${i}" title="Remove">×</button>`;
    fileListEl.appendChild(li);
  });
  fileListEl.querySelectorAll('.file-remove').forEach(b =>
    b.addEventListener('click', () => { fileQueue.splice(+b.dataset.i, 1); renderQueue(); })
  );
}

function setStatus(i, type, text) {
  const el = $('fs-' + i);
  if (el) { el.className = 'file-status ' + type; el.textContent = text; }
}

function addFiles(newFiles) {
  const existing = new Set(fileQueue.map(f => f.name));
  let added = 0;
  for (const f of newFiles) {
    if (/\.(step|stp)$/i.test(f.name) && !existing.has(f.name)) {
      fileQueue.push(f);
      existing.add(f.name);
      added++;
    }
  }
  if (!added && newFiles.length) log('warn', '⚠ No new .step/.stp files found in selection');
  renderQueue();
}

// Drag & Drop
dropzone.addEventListener('dragover', e => {
  e.preventDefault();
  dropzone.classList.add('drag-over');
  scanLine.style.animationPlayState = 'running';
});

dropzone.addEventListener('dragleave', () => {
  dropzone.classList.remove('drag-over');
  scanLine.style.animationPlayState = 'paused';
});

dropzone.addEventListener('drop', async e => {
  e.preventDefault();
  // Keep the scanning visual active while we recursively read the dropped
  // folder/files - this can take a noticeable moment for large folders, and
  // killing the animation immediately on drop made the box look frozen.
  dropzone.classList.add('drag-over');
  scanLine.style.animationPlayState = 'running';
  try {
    const files = await getFilesFromDataTransfer(e.dataTransfer);
    addFiles(files);
  } finally {
    dropzone.classList.remove('drag-over');
    scanLine.style.animationPlayState = 'paused';
  }
});

dropzone.addEventListener('click', e => { if (e.target !== browseBtn) fileInput.click(); });
dropzone.addEventListener('keydown', e => { if (e.key === 'Enter' || e.key === ' ') { e.preventDefault(); fileInput.click(); } });
browseBtn.addEventListener('click', e => { e.stopPropagation(); fileInput.click(); });
fileInput.addEventListener('change', () => { addFiles(Array.from(fileInput.files)); fileInput.value = ''; });

clearBtn.addEventListener('click', () => { fileQueue = []; renderQueue(); });
logClearBtn.addEventListener('click', () => { logBody.innerHTML = ''; logPanel.hidden = true; });

// Convert
convertBtn.addEventListener('click', async () => {
  if (!fileQueue.length) return;
  convertBtn.disabled = true;
  clearBtn.disabled   = true;

  log('head', `── Converting ${fileQueue.length} file(s) ──`);
  log('dim',  '  Loading WASM kernel (first run ~5 s, then cached)…');

  let oc;
  try {
    oc = await loadOpenCascade();
  } catch (err) {
    log('error', '✗ ' + err.message);
    convertBtn.disabled = false;
    clearBtn.disabled   = false;
    return;
  }

  const stlResults = [], origBuffers = [];
  let ok = 0, fail = 0;

  for (let i = 0; i < fileQueue.length; i++) {
    const file = fileQueue[i];
    setStatus(i, 'working', 'converting…');
    log('info', '  → ' + file.name);
    try {
      const buf     = await file.arrayBuffer();
      const stlData = convertStepToStl(oc, buf, file.name);
      const stlName = file.name.replace(/\.(step|stp)$/i, '.stl');
      stlResults.push({ name: stlName, data: stlData });
      origBuffers.push({ name: file.name, data: new Uint8Array(buf) });
      setStatus(i, 'done', '✓ done');
      log('success', `  ✓ ${stlName} (${fmt(stlData.length)})`);
      ok++;
    } catch (err) {
      setStatus(i, 'error', '✗ error');
      log('error', `  ✗ ${file.name}: ${err.message}`);
      fail++;
    }
  }

  log('head', `── ${ok} converted, ${fail} failed ──`);

  if (stlResults.length) {
    log('dim', '  Building ZIP…');
    const zip = window.buildZip(stlResults, origBuffers);
    const name = stlResults.length === 1
      ? stlResults[0].name.replace('.stl', '') + '_step2stl.zip'
      : 'step2stl_output.zip';
    downloadBlob(zip, name, 'application/zip');
    log('success', `✓ Downloaded: ${name}`);
    log('dim',     '  STL files at root · originals in originals/');
  }

  convertBtn.disabled = false;
  clearBtn.disabled   = false;
  fileQueue = [];
  renderQueue();
});
